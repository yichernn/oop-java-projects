    private void btnGenerateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerateActionPerformed
        String selectedMonth = cmbMonth.getSelectedItem().toString();
        String selectedYear = cmbYear.getSelectedItem().toString();

        int monthNumber = Month.valueOf(selectedMonth.toUpperCase()).getValue(); 

        List<Order> orders = order.readOrderFile(); 
        List<Order> filteredOrders = new ArrayList<>();

        for (Order o : orders) {
            if ("Paid".equalsIgnoreCase(o.getStatus()) && o.getVerifiedDate() != null && !o.getVerifiedDate().isEmpty()) {
                LocalDate paymentDate = LocalDate.parse(o.getVerifiedDate());
                if (paymentDate.getMonthValue() == monthNumber && paymentDate.getYear() == Integer.parseInt(selectedYear)) {
                    filteredOrders.add(o);
                }
            }
        }

        populateReportTable(filteredOrders);
    }//GEN-LAST:event_btnGenerateActionPerformed

