    private void populateReportTable(List<Order> orders) {
        DefaultTableModel model = (DefaultTableModel) tblReport.getModel();
        model.setRowCount(0);
        
        int orderCount = 0;
        double subtotal = 0.0;

        for (Order o : orders) {
            model.addRow(new Object[]{
                o.getOrderID(),
                o.getSupplierID(),
                o.getItemID(),
                o.getQuantity(),
                o.getPricePerUnit(),
                o.getTotal(),
                o.getVerifiedDate()
            });
            
            orderCount++;
            subtotal += o.getTotal();
        }
        txtTotalOrder.setText(String.valueOf(orderCount));
        txtTotalRevenue.setText(String.format("RM %.2f", subtotal));
    }

