    public void loadPaymentSummary() {
        fullSummaryList = order.getOrderVerificationSummary();
        populateTable(tblPayment, fullSummaryList);
    }
    
    private void populateTable(javax.swing.JTable table, List<String[]> data) {
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.setRowCount(0);

        for (String[] row : data) {
            model.addRow(row);
        }
    }
    
    private void searchOrder(String orderID) {
        if (orderID == null || orderID.trim().isEmpty()) {
            populateTable(tblPayment, fullSummaryList);
            return;
        }

        List<String[]> filtered = new java.util.ArrayList<>();
        for (String[] row : fullSummaryList) {
            if (row[0].equalsIgnoreCase(orderID.trim())) {
                filtered.add(row);
            }
        }

        populateTable(tblPayment, filtered);
    }

