    private void btnPaymentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPaymentActionPerformed
        int selectedRow = tblPayment.getSelectedRow();

        if (selectedRow != -1) {
            String orderID = (String) tblPayment.getValueAt(selectedRow, 0);

            if (order.isOrderReadyForPayment(orderID)) {
                List<Order> orderDetails = order.getFullOrderInfo(orderID);
                new FMPaymentConfirmation(uname, orderID,orderDetails,this).setVisible(true); 
            } else {
                JOptionPane.showMessageDialog(this,"Cannot proceed. Not all items in this order are verified.","Payment Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this,"Please select an order to make payment.","Selection Required", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnPaymentActionPerformed

