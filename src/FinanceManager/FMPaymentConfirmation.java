    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
        boolean success = order.updateOrderStatus(orderID, "Paid", Date);
        if (success) {

            int choice = JOptionPane.showConfirmDialog(
                this,
                "Payment " + paymentID + " successfully paid.\nDo you want to download the invoice?",
                "Download Invoice",
                JOptionPane.YES_NO_OPTION
            );

            if (choice == JOptionPane.YES_OPTION) {
                fm.exportInvoice(
                    paymentID,
                    orderID,
                    txtSupplierID.getText(),
                    txtFMName.getText(),
                    txtFMId.getText(),
                    Date,
                    tblItem.getModel(),
                    txtSubtotal.getText()
                );
                JOptionPane.showMessageDialog(this, "Invoice exported successfully.");
            }

            fmpayment.loadPaymentSummary();
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Failed to confirm payment.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnConfirmActionPerformed

