    private void populateOrderTable(List<Order> orderDetails) {
        DefaultTableModel model = (DefaultTableModel) tblItem.getModel();
        model.setRowCount(0);

        double subtotal = 0.0;

        for (Order o : orderDetails) {
            double pricePerUnit = o.getPricePerUnit(); 
            int quantity = o.getQuantity();
            double total = pricePerUnit * quantity;

            Object[] row = new Object[5];
            row[0] = o.getItemID();
            row[1] = quantity;
            row[2] = String.format("RM %.2f", pricePerUnit);
            row[3] = String.format("RM %.2f", total);

            subtotal += total;
            model.addRow(row);
        }

        txtSubtotal.setText(String.format("%.2f", subtotal));
    }


