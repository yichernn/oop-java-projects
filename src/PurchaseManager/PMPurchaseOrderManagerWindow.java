    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        if (btnEdit.getText().equals("Edit")) {
            int row = tblPOs.getSelectedRow();
            if (row == -1) {
                JOptionPane.showMessageDialog(this, "Please select a row to edit.");
                return;
            }
            // Enable text fields and chnage it colour
            txtQuantity.setEditable(true);
            txtQuantity.setBackground(Color.WHITE);
            //change the text 'edit' button to 'save edit' 
            btnEdit.setText("Save Edit");
        } else {
            // Validate inputs
            if (!pm.isPositiveInteger(txtQuantity.getText(), "Quantity") ||
                !pm.isPositiveDouble(txtUnitPrice.getText()) ||
                !pm.isValidID(txtPOID.getText(), "PO", 3, "PO ID", this) ||
                !pm.isValidID(txtItemID.getText(), "ITEM", 3, "Item ID", this) ||
                !pm.isValidID(txtSupplierID.getText(), "SUP", 3, "Supplier ID", this) ||
                !pm.isValidID(txtRequisitionID.getText(),"PR", 3, "Requisition ID", this)) {
                return;
            }

            // Update row
            int row = tblPOs.getSelectedRow();
            DefaultTableModel model = (DefaultTableModel) tblPOs.getModel();
            JTextField[] fields = {
                txtPOID, txtItemID, txtItemName,
                txtQuantity, txtSupplierID,
            };
            for (int i = 0; i < 4; i++) {
                model.setValueAt(fields[i].getText(), row, i);
            }

            // Update total, date, status
            double quantity = Double.parseDouble(txtQuantity.getText());
            double unitPrice = Double.parseDouble(txtUnitPrice.getText());
            double total = quantity * unitPrice;
            model.setValueAt(String.format("%.2f", total), row, 8);
            model.setValueAt(java.time.LocalDate.now().toString(), row, 9);
            model.setValueAt("Pending", row, 10);
            model.setValueAt("Lee", row,11);

            txtQuantity.setEditable(false);
            txtQuantity.setBackground(new Color(204, 204, 204));
            btnEdit.setText("Edit");
        }
    }//GEN-LAST:event_btnEditActionPerformed

