    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String query = txtSearch.getText().trim().toLowerCase();
        DefaultTableModel model = (DefaultTableModel) tblPOs.getModel();
        TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(model);
        tblPOs.setRowSorter(sorter);

        if (query.isEmpty()) {
            sorter.setRowFilter(null); // Show all
        } else if (query.equals("rejected")) {
            sorter.setRowFilter(RowFilter.regexFilter("(?i)rejected", 9)); // Column 9 = status
        } else {
            sorter.setRowFilter(RowFilter.regexFilter("(?i)" + query, 0)); // Column 0 = PO ID
        }
    }//GEN-LAST:event_btnSearchActionPerformed

