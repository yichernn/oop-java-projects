    private void handleClose() {
        int option = JOptionPane.showConfirmDialog(
            this,
            "Do you want to save changes before closing?",
            "Confirm Exit",
            JOptionPane.YES_NO_CANCEL_OPTION,
            JOptionPane.QUESTION_MESSAGE
        );

        if (option == JOptionPane.YES_OPTION) {
            DefaultTableModel model = (DefaultTableModel) tblPOs.getModel();
            List<String[]> data = new ArrayList<>();
            for (int i = 0; i < model.getRowCount(); i++) {
            String[] row = new String[12];
                for (int j = 0; j < 12; j++) {
                    row[j] = model.getValueAt(i, j).toString();
                }
                data.add(row);
            }
            pm.writeFile("PO.txt", data);
            JOptionPane.showMessageDialog(this, "Purchase orders saved.");
            dispose();
        } else if (option == JOptionPane.NO_OPTION) {
            dispose();
        }
        // If CANCEL or X clicked, do nothing
    }
