    private void tblPOsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPOsMouseClicked
        int row = tblPOs.getSelectedRow();
        if (row != -1) {
            DefaultTableModel model = (DefaultTableModel) tblPOs.getModel();

            // Map only required indices
            int[] indices = {0, 1, 2, 3, 4, 6, 7, 11};
            JTextField[] fields = {
                txtPOID, txtItemID, txtItemName, txtQuantity,
                txtSupplierID, txtRequisitionID, txtUnitPrice, txtPMName
            };

            for (int i = 0; i < fields.length; i++) {
                fields[i].setText(model.getValueAt(row, indices[i]).toString());
            }
        }
    }//GEN-LAST:event_tblPOsMouseClicked

